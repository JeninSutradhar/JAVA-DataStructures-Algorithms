package Graph.AdjecencyList;

/**
 * A node class for Adjecency List Graph Data Structure
 * 
 * It contains a single character, data, which is the data stored in that node
 */
public class Node {
    /**
     * Data stored in this node
     */
    char data;

    /**
     * Constructor for Node class
     * @param data The data to be stored in the node
     */
    Node (char data) {
        this.data = data;
    }
}

